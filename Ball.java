public class Ball {
    private Team theTeam;
    private Person thePlayer;
    private double theTime;
    private int theStatus;

    public Team getTheTeam() {
        return theTeam;
    }

    public void setTheTeam(Team theTeam) {
        this.theTeam = theTeam;
    }

    public Person getThePlayer() {
        return thePlayer;
    }

    public void setThePlayer(Person thePlayer) {
        this.thePlayer = thePlayer;
    }

    public double getTheTime() {
        return theTime;
    }

    public void setTheTime(double theTime) {
        this.theTime = theTime;
    }

    public int getTheStatus() {
        return theStatus;
    }

    public void setTheStatus(int theStatus) {
        this.theStatus = theStatus;
    }
}
